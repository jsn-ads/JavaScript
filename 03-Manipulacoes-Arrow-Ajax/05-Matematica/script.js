let x;

// valor de PI

x = Math.PI;

console.log(x);

// Arredonda o valor pra cima ou para baixo aparti do valor do meio

x = Math.round(3.52);

console.log(x);

// sempre arredonda para baixo

x = Math.floor(3.89);

console.log(x);

// sempre arredonda para cima 

x = Math.ceil(3.21);

console.log(x);

// retorna o positivo

x = Math.abs(-7.839);

console.log(x);

// retorna o menor numero da lista

x = Math.min(8,673,77,202, 3);

console.log(x);

// retorna o maior numero da lista

x = Math.max(8,673,77,202, 3);

console.log(x);

// retorna um numero aleatorio 

x = Math.floor(Math.random() * 100);

console.log(x);


